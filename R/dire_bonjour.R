#' Dire bonjour
#'
#' @param entree a string
#'
#' @return a string
#' @import glue
#' @export
#' @examples
#' #' dire_bonjour("Sabine")
#'
dire_bonjour <- function(entree){

  if(!is.character(entree)){
    stop("Mauvais format \n saisir une chaine de caracteres")
  }

  glue("Bonjour {entree}")
}
